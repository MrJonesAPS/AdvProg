public class Casting{

   public static void main(String[] args){
      
      float twoThirds = 2/3;
      
      System.out.println(twoThirds);
      
      float twoThirdsTakeTwo = (float)2/3;
      
      System.out.println(twoThirdsTakeTwo);
      
      double twoThirdsTake3 = 2.0/3.0;
      
      System.out.println(twoThirdsTake3);
      
      float twoThirdsTake4 = 2.0f/3;
      
      System.out.println(twoThirdsTake4);
   
   }

}